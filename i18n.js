/**
 * Safe rich-text setter. Only interprets <em>...</em> and <br>/<br/>; everything
 * else becomes a literal text node. Source strings are authored in this file;
 * no user input ever reaches this function.
 */
function setRichText(el, src) {
  while (el.firstChild) el.removeChild(el.firstChild);
  const parts = src.split(/(<em>[\s\S]*?<\/em>|<br\s*\/?>)/g);
  for (const part of parts) {
    if (!part) continue;
    if (/^<br\s*\/?>$/i.test(part)) {
      el.appendChild(document.createElement("br"));
      continue;
    }
    const emMatch = part.match(/^<em>([\s\S]*?)<\/em>$/i);
    if (emMatch) {
      const node = document.createElement("em");
      node.textContent = emMatch[1];
      el.appendChild(node);
      continue;
    }
    el.appendChild(document.createTextNode(part));
  }
}

function applyLang(lang) {
  if (lang !== "ja" && lang !== "en") lang = "ja";
  const dict = I18N[lang];
  if (!dict) return;
  document.documentElement.lang = lang === "en" ? "en" : "ja";

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key] !== undefined) el.textContent = dict[key];
  });
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.getAttribute("data-i18n-html");
    if (dict[key] !== undefined) setRichText(el, dict[key]);
  });

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    const active = btn.dataset.lang === lang;
    btn.classList.toggle("active", active);
    btn.setAttribute("aria-pressed", String(active));
  });

  try { localStorage.setItem("airank:lang", lang); } catch (e) {}
}

(function i18nInit() {
  let saved = null;
  try { saved = localStorage.getItem("airank:lang"); } catch (e) {}
  const browserLang = (navigator.language || "ja").toLowerCase().startsWith("en") ? "en" : "ja";
  const initial = saved || browserLang || "ja";
  applyLang(initial);

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.addEventListener("click", () => applyLang(btn.dataset.lang));
  });
})();
